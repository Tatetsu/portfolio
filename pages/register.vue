<template>
  <div>
    <div class="bg-gray-100 flex-auto">
      <div class="flex justify-center mt-16">
        <div class="w-2/5 border bg-white">
          <div class="my-12 text-center">
            <h2 class="text-4xl font-bold">ユーザー登録画面</h2>
                <div class="google-login my-5 flex justify-center">
                <button
                    type="submit"
                    @click="googleLogin"
                    class="text-sm w-3/5 text-gray-300 py-2 border rounded flex justify-center items-center"
                >
                    <img
                    src="https://madeby.google.com/static/images/google_g_logo.svg"
                    class="button-logo-img mr-4"
                    style="height: 24px"
                    />
                    Googleでログインする
                </button>
                </div>
            <p class="my-4">
              <span class="font-semibold">メールアドレス</span>と
              <span class="font-semibold">パスワード</span>を入力してください。
            </p>
            <form @submit.prevent="register" novalidate>
              <div class="mb-2">
                <input
                  type="mail"
                  name="mail"
                  required="required"
                  v-model="email"
                  @input="isInput"
                  placeholder="you@example.com"
                  class="text-xl w-3/5 p-3 border rounded"
                  autofocus
                />
                <p class="text-red-400">{{ emailErrorMassage }}</p>
              </div>
              <div class="mb-2">
                <input
                  type="password"
                  name="password"
                  required="required"
                  v-model="password"
                  @input="isInput"
                  placeholder="パスワード"
                  class="text-xl w-3/5 p-3 border rounded"
                />
                <p class="text-red-400">{{ passwordErrorMassage }}</p>
              </div>
              <button
                type="submit"
                @click="register"
                class="text-xl w-3/5 bg-green-800 text-white py-2 rounded"
              >
                ユーザーの登録
              </button>
            </form>
          </div>
        </div>
      </div>
    </div>
  </div>
</template>

<script>
export default {
  computed: {
    user() {
      return this.$store.getters["user"];
    },
  },
  data: function () {
    return {
      email: "",
      password: "",
      emailErrorMassage: "",
      passwordErrorMassage: "",
      emailRegexp: /^[a-z\d][\w.-]*@[\w.-]+\.[a-z\d]+$/i,
      passwordRegexp: /^(?=.*?[a-z])(?=.*?\d)[a-z\d]{8,20}$/i,
    };
  },
  methods: {
    googleLogin() {
      const provider = new this.$firebase.auth.GoogleAuthProvider();
      this.$auth
        .signInWithPopup(provider)
        .then(() => {
          alert("ログインに成功しました");
          this.$store.dispatch("checkLogin");
          this.$router.push("/mypage");
        })
        .catch((error) => {
          console.log(error);
          alert(
            "エラーが発生いたしました。間違い等がないか確認をし再度実施をお願いします"
          );
        });
    },

    register() {
      if (!this.emailRegexp.test(this.email)) {
        this.emailErrorMassage =
          "このメールアドレスは無効です。正しく入力してください。";
      }
      if (!this.passwordRegexp.test(this.password)) {
        this.passwordErrorMassage =
          "このパスワードは無効です。半角英数字を含んで8〜20文字の範囲で入力してください。";
      }
      if (this.email === "") {
        this.emailErrorMassage = "メールアドレスを入力してください";
      }
      if (this.password === "") {
        this.passwordErrorMassage = "パスワードを入力してください";
      }
      if (this.emailErrorMassage !== "" || this.passwordErrorMassage !== "") {
        return;
      }

      this.$auth
        .createUserWithEmailAndPassword(this.email, this.password)
        .then(() => {
          alert("登録が完了しました");
          this.$store.dispatch("checkLogin");
          this.$router.push("/mypage");
        })
        .catch((error) => {
          console.log({ code: error.code, message: error.message });
          if (error.code === "auth/invalid-email") {
            this.emailErrorMassage = "このメールアドレスは無効です";
          } else if (error.code === "auth/email-already-in-use") {
            this.emailErrorMassage =
              "このメールアドレスはすでに使用されています";
          } else {
            alert(
              "エラーにより登録ができませんでした。恐れ入りますが再度お試しください"
            );
          }
        });
    },
    isInput() {
      this.emailErrorMassage = "";
      this.passwordErrorMassage = "";
    },
  },
};
</script>

